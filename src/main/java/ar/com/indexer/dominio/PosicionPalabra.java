package ar.com.indexer.dominio;

public class PosicionPalabra {

	int width;
	int height;
	int x;
	int y;
	
	int Iwidth;
	int Iheight;
	int Ix;
	int Iy;
	

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getIwidth() {
		return Iwidth;
	}

	public void setIwidth(int iwidth) {
		Iwidth = iwidth;
	}

	public int getIheight() {
		return Iheight;
	}

	public void setIheight(int iheight) {
		Iheight = iheight;
	}

	public int getIx() {
		return Ix;
	}

	public void setIx(int ix) {
		Ix = ix;
	}

	public int getIy() {
		return Iy;
	}

	public void setIy(int iy) {
		Iy = iy;
	}

	
	
	
}
